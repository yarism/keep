const { contextBridge, ipcRenderer } = require('electron');

contextBridge.exposeInMainWorld('git', {
  openRepo: () => ipcRenderer.invoke('open-repo'),
  loadRepos: () => ipcRenderer.invoke('load-repos'),
  saveRepos: (repos) => ipcRenderer.invoke('save-repos', repos),
  loadSettings: () => ipcRenderer.invoke('load-settings'),
  saveSettings: (s) => ipcRenderer.invoke('save-settings', s),
  status: (p) => ipcRenderer.invoke('git-status', p),
  log: (p, branch, limit) => ipcRenderer.invoke('git-log', p, branch, limit),
  branches: (p) => ipcRenderer.invoke('git-branches', p),
  tags: (p) => ipcRenderer.invoke('git-tags', p),
  remotes: (p) => ipcRenderer.invoke('git-remotes', p),
  stashes: (p) => ipcRenderer.invoke('git-stashes', p),
  diff: (p, file, staged) => ipcRenderer.invoke('git-diff', p, file, staged),
  commitDetail: (p, hash) => ipcRenderer.invoke('git-commit-detail', p, hash),
  commitDiff: (p, hash) => ipcRenderer.invoke('git-commit-diff', p, hash),
  commitFiles: (p, hash) => ipcRenderer.invoke('git-commit-files', p, hash),
  commitFileDiff: (p, hash, file) => ipcRenderer.invoke('git-commit-file-diff', p, hash, file),
  searchLog: (p, query, field, branch, limit) => ipcRenderer.invoke('git-search-log', p, query, field, branch, limit),
  stage: (p, file) => ipcRenderer.invoke('git-stage', p, file),
  unstage: (p, file) => ipcRenderer.invoke('git-unstage', p, file),
  stageAll: (p) => ipcRenderer.invoke('git-stage-all', p),
  commit: (p, msg) => ipcRenderer.invoke('git-commit', p, msg),
  checkout: (p, branch) => ipcRenderer.invoke('git-checkout', p, branch),
  createBranch: (p, name, from) => ipcRenderer.invoke('git-create-branch', p, name, from),
  deleteBranch: (p, name) => ipcRenderer.invoke('git-delete-branch', p, name),
  renameBranch: (p, oldN, newN) => ipcRenderer.invoke('git-rename-branch', p, oldN, newN),
  merge: (p, branch) => ipcRenderer.invoke('git-merge', p, branch),
  rebase: (p, branch) => ipcRenderer.invoke('git-rebase', p, branch),
  pull: (p) => ipcRenderer.invoke('git-pull', p),
  push: (p) => ipcRenderer.invoke('git-push', p),
  fetch: (p) => ipcRenderer.invoke('git-fetch', p),
  stashSave: (p, msg) => ipcRenderer.invoke('git-stash-save', p, msg),
  stashApply: (p, idx) => ipcRenderer.invoke('git-stash-apply', p, idx),
  stashDrop: (p, idx) => ipcRenderer.invoke('git-stash-drop', p, idx),
  revert: (p, hash) => ipcRenderer.invoke('git-revert', p, hash),
  createTag: (p, name, ref) => ipcRenderer.invoke('git-create-tag', p, name, ref),
  stageHunk: (p, file, hunk) => ipcRenderer.invoke('git-stage-hunk', p, file, hunk),
  discardHunk: (p, file, hunk) => ipcRenderer.invoke('git-discard-hunk', p, file, hunk),
  discardFile: (p, file) => ipcRenderer.invoke('git-discard-file', p, file),
  trashFile: (p, file) => ipcRenderer.invoke('git-trash-file', p, file),
  showInFinder: (p, file) => ipcRenderer.invoke('git-show-in-finder', p, file),
});
